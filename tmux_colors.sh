    light)
        export TMUX_COLOR_STATUS_BG=$SOLARIZED_BASE2_BG
        export TMUX_COLOR_STATUS_FG=$SOLARIZED_BASE1_FG
        export TMUX_COLOR_STATUS_ATTR=$SOLARIZED_BASE1_ATTR

